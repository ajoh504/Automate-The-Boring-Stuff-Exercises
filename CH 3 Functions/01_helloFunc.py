# This is an example of defining a function and calling it.
# The 'def' keyword tells the interpreter to define the function.
# hello() is the name of our function.
# The three 'print' statements compose the body of the function
# The function is called three times.

def hello():
    print('Howdy!')
    print('Howdy!!!')
    print('Hello there.')

hello()
hello()
hello()
